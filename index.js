// Déclaration d'une variable
//var, let, const

//var : réaffecter, redéclarer, accéder de partout.
var test = "test";

//let : réaffecter, accéder de partout, pas redéclarer.
let test2 = "test2";

//const : pas réaffecter, pas redéclarer, accéder de partout.
const test3 = "test3";

// CamelCase
const monTitre = document.getElementById("titre");

console.log(monTitre);